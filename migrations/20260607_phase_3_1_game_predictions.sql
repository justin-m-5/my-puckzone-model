create table if not exists public.game_predictions (
    id bigint generated by default as identity primary key,
    game_id integer not null references public.games(id),
    predicted_at timestamptz not null default now(),
    game_date date not null,
    game_type integer not null,
    home_team_id integer not null,
    away_team_id integer not null,
    home_goalie_id integer null,
    away_goalie_id integer null,
    home_win_prob double precision not null,
    away_win_prob double precision not null,
    pred_home_goals double precision not null,
    pred_away_goals double precision not null,
    predicted_winner_team_id integer null,
    model_versions jsonb not null default '{}'::jsonb,
    input_snapshot jsonb not null default '{}'::jsonb,
    output_snapshot jsonb not null default '{}'::jsonb,
    status text not null default 'ok',
    error_message text null,
    created_at timestamptz not null default now()
);

create unique index if not exists game_predictions_game_id_key
    on public.game_predictions (game_id);
